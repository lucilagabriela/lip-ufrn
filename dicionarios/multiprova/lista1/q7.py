# Você quer lembrar as datas dos aniversários dos seus amigos. Para isso, você decidiu escrever um programa que cria um dicionário com o aniversário de cada pessoa. O seu programa deve ler as informações sobre o nome e a data de nascimento (dia e mês) de cada pessoa. Você deve criar um dicionário que associa a cada pessoa a sua data de nascimento.
# Em seguida, seu programa deve ler o valor de um mês e imprimir as pessoas que fazem aniversário naquele mês.
# Para a entrada dos dados, o seu programa deve adicionar pessoas enquanto o usuário não fornecer o valor 'sair' para o nome de uma pessoa. Esse valor não deve ser considerado.
# As informações de uma pessoa serão fornecidas em duas linhas, onde na primeira linha temos o nome da pessoa e na segunda a sua data de nascimento (dia e mês).
# Após o usuário fornecer o valor 'sair', ele indicará um mês e o seu programa deve imprimir as pessoas que fazem aniversário nele.
# - Exemplo de Entrada
# João
# 12 dezembro
# Maria
# 15 janeiro
# Bartolomeu
# 25 dezembro
# sair
# dezembro
# - Exemplo de Saída
# Aniversariantes de dezembro
# 12 João
# 25 Bartolomeu
